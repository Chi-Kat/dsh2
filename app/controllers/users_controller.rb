class UsersController < ApplicationController

before_filter :require_login, :except => [:index, :show]
#before_filter :require_authorization, :only => [:edit, :update]
before_filter :require_auth_or_admin, :only => [:new, :create, :destroy, :edit, :update]

def current_user
    User.find_by_id(session["user_id"])
  end

def require_login
     if current_user.present?
     else redirect_to root_url, notice: 'Please login.' 
    end
  end

# def require_authorization
#   if session[:user_id] == params["id"].to_i
#   else redirect_to root_url, notice: 'Require authorization'
#   end
# end

def require_auth_or_admin
  if session["user_id"] == params["id"].to_i || current_user.type == "Admin"
  else redirect_to root_url, notice: 'Not auth or admin.' 
  end
end

  def index
    @users = User.all

    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @users }
    end
  end

  # GET /users/1
  # GET /users/1.json
  def show
    @user = User.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @user }
    end
  end

  # GET /users/new
  # GET /users/new.json
  def new
    @user = User.new

    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @user }
    end
  end

  # GET /users/1/edit
  def edit
    @user = User.find(params[:id])
  end

  # POST /users
  # POST /users.json
  def create
    @user = User.new(params[:user])

    respond_to do |format|
      if @user.save
        session[:user_id] = @user.id
        format.html { redirect_to @user, notice: 'User was successfully created.' }
        format.json { render json: @user, status: :created, location: @user }
      else
        format.html { render action: "new" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /users/1
  # PUT /users/1.json
  def update
    @user = User.find(params[:id])

    respond_to do |format|
      if @user.update_attributes(params[:user])
        format.html { redirect_to @user, notice: 'User was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1
  # DELETE /users/1.json
  def destroy
    @user = User.find(params[:id])
    @user.destroy

    respond_to do |format|
      format.html { redirect_to users_url }
      format.json { head :no_content }
    end
  end
end
